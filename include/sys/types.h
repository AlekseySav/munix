#ifndef _SYS_TYPES_H_
#define _SYS_TYPES_H_

#ifndef _INT8_T_
#define _INT8_T_
typedef signed char int8_t;
#endif

#ifndef _UINT8_T_
#define _UINT8_T_
typedef unsigned char uint8_t;
#endif

#ifndef _INT16_T_
#define _INT16_T_
typedef signed short int16_t;
#endif

#ifndef _UINT16_T_
#define _UINT16_T_
typedef unsigned short uint16_t;
#endif

#ifndef _INT32_T_
#define _INT32_T_
typedef signed long int32_t;
#endif

#ifndef _UINT32_T_
#define _UINT32_T_
typedef unsigned long uint32_t;
#endif

#endif
